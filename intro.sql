CREATE TABLE FUNCIONARIOS (
	FUN_CODIGO INT NOT NULL PRIMARY KEY,
	FUN_SALARIO DECIMAL(7,2) CHECK (FUN_SALARIO>0),
	FOREIGN KEY (FUN_CODIGO) REFERENCES PESSOAS (PES_CODIGO)
);

--IDENTITY: Controle de auto-incremento (inicia com o valor 1)
--NOT NULL: Define o campo como obrigat¾rio
--UNIQUE: Garante que o valor nŃo serß repitido

CREATE TABLE PRODUTOS (
	PRD_CODIGO INT NOT NULL PRIMARY KEY IDENTITY,
	PRD_DESCICAO VARCHAR(60) NOT NULL,
	PRD_QTD INT,
	PRD_VALOR DECIMAL (7,2) CHECK(PRD_VALOR>0),
	PRD_STATUS INT CHECK (PRD_STATUS IN (1,2,3,4,5))
);

CREATE TABLE PEDIDOS (
	PED_NUMERO INT NOT NULL PRIMARY KEY IDENTITY,
	PED_DATA DATETIME, --DATE
	PED_VALOR DECIMAL(7,2),
	PED_STATUS INT NOT NULL CHECK (PED_STATUS IN (1,2,3,4,5)),
	FUN_CODIGO INT,
	CLI_CODIGO INT,
	FOREIGN KEY (FUN_CODIGO) REFERENCES FUNCIONARIOS (FUN_CODIGO),
	FOREIGN KEY (CLI_CODIGO) REFERENCES CLIENTES (CLI_CODIGO)
);

CREATE TABLE ITENS_PEDIDOS (
	PED_NUMERO INT NOT NULL,
	PRD_CODIGO INT NOT NULL,
	IP_QTD INT NOT NULL,
	IP_VALOR DECIMAL(7,2) NOT NULL,
	PRIMARY KEY (PED_NUMERO, PRD_CODIGO), --Chave-composta
	FOREIGN KEY (PED_NUMERO) REFERENCES PEDIDOS (PED_NUMERO),
	FOREIGN KEY (PRD_CODIGO) REFERENCES PRODUTOS (PRD_CODIGO)
	--FOREIGN KEY PED_NUMERO
	--FOREIGN KEY PRD_CODIGO
);

