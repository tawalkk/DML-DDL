CREATE TABLE CURSO (
	CUR_CODCURSO INT NOT NULL PRIMARY KEY,
	CUR_NOME VARCHAR(20) NOT NULL,
	CUR_PONTUACAO DECIMAL(4,2)

);

CREATE TABLE PARTICIPANTE ( 
	PAR_CPF VARCHAR(11) NOT NULL PRIMARY KEY,
	PAR_MATRICULA INT NOT NULL,
	PAR_NOME VARCHAR(30) NOT NULL
);

CREATE TABLE TURMA (
	TUR_CODTURMA INT NOT NULL PRIMARY KEY,
	TUR_SALA INT NOT NULL,
	TUR_HORARIO DATETIME,
	TUR_CPFINSTRUTOR VARCHAR(11),
	TUR_CODCURSO INT,
	FOREIGN KEY (TUR_CODCURSO) REFERENCES CURSO (CUR_CODCURSO),
	FOREIGN KEY (TUR_CPFINSTRUTOR) REFERENCES INSTRUTOR (INS_CPF)
	
);

CREATE TABLE MATRICULA (
	PAR_CPF VARCHAR(11),
	TUR_CODTURMA INT,
	PRIMARY KEY (PAR_CPF, TUR_CODTURMA),
	FOREIGN KEY (PAR_CPF) REFERENCES PARTICIPANTE (PAR_CPF),
	FOREIGN KEY (TUR_CODTURMA) REFERENCES TURMA (TUR_CODTURMA)
);


